package SOF304.SOF304_Lab1;

public class Caculator {
    public int add(int a,int b) {
    	int c = a+ b;
    	return c;
    }
    public int sub(int a,int b) {
    	int c = a -  b;
    	return c;
    }
    public int mul(int a,int b) {
    	int c = a * b;
    	return c;
    }
    public int div(int a,int b) {
    	int c = a / b;
    	return c;
    }
    
}
