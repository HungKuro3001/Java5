package LAB.Lab01_SOF303;

public class Caculator {
	public int tong(int x, int y) {
		int z = x + y;
		return z;
	}
	public int tru(int x, int y) {
		int z = x - y;
		return z;
	}
	public int nhan(int x, int y) {
		int z = x * y;
		return z;
		
	}
	public int chia(int x, int y) {
		int z = x/y;
		return z;
	}
}
